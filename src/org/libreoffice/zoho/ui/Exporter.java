    private void hidePanel(JPanel panel, boolean hide) {
        panel.setEnabled(!hide);
        panel.setVisible(!hide);
    }

    private void setGraphics() {
        ImageIcon zohoIcon;
        

        zohoIcon = new ImageIcon(getClass().getResource("/org/libreoffice/zoho/Resources/zoho_logo.png"));
        Image img = zohoIcon.getImage();
        Image newimg = img.getScaledInstance(zohoIcon.getIconWidth() / 2, zohoIcon.getIconHeight() / 2, java.awt.Image.SCALE_SMOOTH);
        zohoLogo = new ImageIcon(newimg);
        this.setIconImage(new ImageIcon(getClass().getResource("/org/libreoffice/zoho/Resources/zoho_icon.png")).getImage());
    }

     private void initUI() {
        hidePanel(jPanel1, true);
        pack();
        exportBtn.requestFocus();
        docFactory = new DocumentFactory();
        uploadDoc = docFactory.createUploadDoc(uploader.getOpenedDocumentPath());
        fileNameField.setText(uploadDoc.getName());
    }
    
