    private void exportBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exportBtnActionPerformed

        Progress uploading = new Progress(new JFrame(), false, "Uploading File...");

        uploader.addObserver(uploading);
        uploading.setVisible(true);
        Logger.log("SetVisible Completed");

        Thread downThread = new Thread() {

            @Override
            public void run() {
                Document uploadedDoc = uploader.Upload(uploadDoc);
                LocalStorage.getInstance().setCurrentDoc(uploadedDoc);
                LocalStorage.getInstance().addToList(uploadedDoc);
            }
        };
        downThread.start();

        this.dispose();
    }//GEN-LAST:event_exportBtnActionPerformed

